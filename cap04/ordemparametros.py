

# Exemplo com ordem de parâmetros
def mostrar_informacao(nome, idade, cidade):
    """Esta função imprime informações pessoais."""
    print(f"{nome} tem {idade} anos e mora em {cidade}.")


# Chamada de função com ordem de argumentos
mostrar_informacao("Carlos", 30, "São Paulo")  # Saída: "Carlos tem 30 anos e mora em São Paulo."


