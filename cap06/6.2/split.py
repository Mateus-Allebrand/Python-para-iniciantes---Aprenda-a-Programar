

# Exemplo de método split
frase = "Python é uma linguagem poderosa"
palavras = frase.split()

print(palavras)  # Saída: ['Python', 'é', 'uma', 'linguagem', 'poderosa']



# Exemplo de split com delimitador específico
data = "2022-01-21"
partes = data.split("-")

print(partes)  # Saída: ['2022', '01', '21']



