
# Exemplo de método format
nome = "Ana"
idade = 25

mensagem = "Olá, meu nome é {} e eu tenho {} anos.".format(nome, idade)
print(mensagem)
# Saída: "Olá, meu nome é Ana e eu tenho 25 anos."


# Exemplo de índices com método format
nome = "Carlos"
idade = 22

mensagem = "Olá, meu nome é {0} e eu tenho {1} anos.".format(nome, idade)
print(mensagem)
# Saída: "Olá, meu nome é Carlos e eu tenho 22 anos."


