

# Exemplo de operações de conjunto
A = {1, 2, 3, 4, 5}
B = {4, 5, 6, 7, 8}

uniao = A.union(B)  # União de conjuntos
intersecao = A.intersection(B)  # Interseção de conjuntos
diferenca = A.difference(B)  # Diferença de conjuntos

print(uniao)  # Saída: {1, 2, 3, 4, 5, 6, 7, 8}
print(intersecao)  # Saída: {4, 5}
print(diferenca)  # Saída: {1, 2, 3}




