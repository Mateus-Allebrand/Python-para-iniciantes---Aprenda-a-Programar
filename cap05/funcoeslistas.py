



# Exemplo de funções de lista
numeros = [3, 1, 4, 1, 5, 9, 2, 6, 5, 3, 5]
print(len(numeros))  # Saída: 11 (número de elementos)
print(min(numeros))  # Saída: 1 (menor elemento)
print(max(numeros))  # Saída: 9 (maior elemento)
print(sum(numeros))  # Saída: 44 (soma de todos os elementos)
numeros.sort()  # Ordenando a lista em ordem crescente
print(numeros)  # Saída: [1, 1, 2, 3, 3, 4, 5, 5, 5, 6, 9]


