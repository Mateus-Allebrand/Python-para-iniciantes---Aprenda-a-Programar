



# Exemplo de criação de conjunto
frutas = {"maçã", "banana", "uva"}
numeros = {1, 2, 3, 4, 5}


# Exemplo de adição e remoção de elementos em conjunto
frutas.add("morango")  # Adicionando "morango" ao conjunto
numeros.remove(3)  # Removendo o elemento 3 do conjunto (gerará um erro se não existir)
numeros.discard(6)  # Removendo o elemento 6 se estiver presente (sem erro se não existir)


